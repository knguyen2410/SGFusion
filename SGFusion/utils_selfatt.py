def RMSE(output, target):
    loss = torch.mean((output-target)**2)
    loss = torch.sqrt(loss)
    return loss


def clip_grad_norm_(parameters, max_norm, norm_type):
    if isinstance(parameters, torch.Tensor):
        parameters = [parameters]
    parameters = list(filter(lambda p: p.grad is not None, parameters))
    max_norm = float(max_norm)
    norm_type = float(norm_type)
    if len(parameters) == 0:
        return torch.tensor(0.)
    device = parameters[0].grad.device
    if norm_type == np.inf:
        total_norm = max(p.grad.detach().abs().max().to(device) for p in parameters)
    else:
        total_norm = torch.norm(torch.stack([torch.norm(p.grad.detach(), norm_type).to(device) for p in parameters]), norm_type)
    clip_coef = max_norm / (total_norm + 1e-6)
    if clip_coef < 1:
        for p in parameters:
            p.grad.detach().mul_(clip_coef.to(p.grad.device))
    return total_norm


def set_seed(seed):
    """for reproducibility
    :param seed:
    :return:
    """
    np.random.seed(seed)
    random.seed(seed)

    torch.manual_seed(seed)
    if torch.cuda.is_available():
        torch.cuda.manual_seed(seed)
        torch.cuda.manual_seed_all(seed)
    torch.backends.cudnn.enabled = False
    torch.backends.cudnn.benchmark = False
    torch.backends.cudnn.deterministic = True


def softmax(e_ij: Dict[str, torch.DoubleTensor]) -> Dict[str, torch.DoubleTensor]:
    res = {}
    denom = [torch.exp(val.type(torch.DoubleTensor)) for val in e_ij.values()]
    denom = torch.stack(denom)
    denom = torch.sum(denom, dim=0)
    for k, v in e_ij.items():
        res[k] = torch.exp(v.type(torch.DoubleTensor)) / denom
    return res


def flatten_weights(weights, device):
        weights_concat = torch.tensor([]).to(device)
        if isinstance(weights, np.ndarray):
            for w in weights:
                w = torch.from_numpy(w).view(-1)
                weights_concat = torch.cat([weights_concat, w.to(device)], dim=-1)
        elif isinstance(weights, dict):
            for w in weights.values():
                w = w.view(-1)
                weights_concat = torch.cat([weights_concat, w.to(device)], dim=-1)
        return weights_concat
        
        
