def train(net, trainloader, lr: float, ustep: int, device: str):
    """Train the network on the training set."""
    inner_optimizer = torch.optim.RMSprop(
        [
            {'params': net.parameters()}
        ], lr=lr, weight_decay=1e-4
    )
    net.train()
    dataset_size = 0
    for step in range(ustep):
        for batch in trainloader:
            if step == 0:
                dataset_size = dataset_size + batch[1].shape[0]

            inputs, target = net.embed_inputs(batch)
            inputs = inputs.float().to(device)
            out = net(inputs, device)
            inner_optimizer.zero_grad()
            loss = RMSE(out, target.to(device))
            loss.backward()
            # clip_grad_norm_(parameters=net.parameters(), max_norm=0.1, norm_type=2.0)
            inner_optimizer.step() 
    return dataset_size



def test(net, testloader, device: str):
    net.eval()
    with torch.no_grad():
        user_loss = 0.
        batches = testloader
        total_sample = 0
        for batch in batches:
            n_sample = batch[1].shape[0]
            total_sample += n_sample
            inputs, target = net.embed_inputs(batch)
            inputs = inputs.float().to(device)
            out = net(inputs)
            user_loss += torch.mean((out-target.to(device))**2)*n_sample
            del batch
        avg_loss = torch.sqrt(user_loss/total_sample)
    return avg_loss, total_sample


